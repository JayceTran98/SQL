-- kHACH HANG --
CREATE TABLE KHACHHANG (
	MAKH CHAR(4) NOT NULL,
	HOTEN varchar(40),
	DCHI varchar(50),
	SODT varchar(20),
	NGSINH smalldatetime,
	DOANHSO money,
	NGDK smalldatetime
);

-- NHAN VIEN --
CREATE TABLE NHANVIEN (
	MANV char(4) NOT NULL,
	HOTEN varchar(40),
	SODT varchar(20),
	NGVL smalldatetime,
);

-- SAN PHAM --
CREATE TABLE SANPHAM (
	MASP char(4) NOT NULL,
	TENSP varchar(40),
	DVT varchar(20),
	NUOCSX varchar(40),
	GIA money
);

-- HOA DON --
CREATE TABLE HOADON (
	SOHD int NOT NULL,
	NGHD smalldatetime,
	MAKH char(4),
	MANV char(4),
	TRIGIA money,
);

-- CTHD --
CREATE TABLE CTHD (
	SOHD int NOT NULL,
	MASP char(4) NOT NULL,
	SL int,
);

-- PRIMARY KEY --
ALTER TABLE KHACHHANG ADD CONSTRAINT PK_KH PRIMARY KEY (MAKH);
ALTER TABLE NHANVIEN ADD CONSTRAINT PK_NV PRIMARY KEY (MANV);
ALTER TABLE SANPHAM ADD CONSTRAINT PK_SP PRIMARY KEY (MASP);
ALTER TABLE HOADON ADD CONSTRAINT PK_HD PRIMARY KEY (SOHD);
ALTER TABLE CTHD ADD CONSTRAINT PK_CTHD PRIMARY KEY (SOHD,MASP);

-- FOREIGN KEY --
ALTER TABLE HOADON ADD CONSTRAINT FK_HD1 FOREIGN KEY (MAKH) REFERENCES KHACHHANG(MAKH);
ALTER TABLE HOADON ADD CONSTRAINT FK_HD2 FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV);
ALTER TABLE CTHD ADD CONSTRAINT FK_CTHD1 FOREIGN KEY (SOHD) REFERENCES HOADON(SOHD);
ALTER TABLE CTHD ADD CONSTRAINT FK_CTHD2 FOREIGN KEY (MASP) REFERENCES SANPHAM(MASP);
 
